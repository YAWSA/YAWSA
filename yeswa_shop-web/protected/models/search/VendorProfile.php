namespace app\models\search;

use Yii;
use yii\base\Model;
use yii\data\ActiveDataProvider;
use app\models\VendorProfile as VendorProfileModel;

/**
 * VendorProfile represents the model behind the search form about `app\models\VendorProfile`.
 */
class VendorProfile extends VendorProfileModel
{
    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['id', 'state_id', 'type_id', 'created_by_id'], 'integer'],
            [['title', 'first_name', 'last_name', 'civil_id', 'description', 'whats_app_no', 'shopname', 'shop_logo', 'created_on', 'updated_on'], 'safe'],
        ];
    }

    /**
     * @inheritdoc
     */
    public function scenarios()
    {
        // bypass scenarios() implementation in the parent class
        return Model::scenarios();
    }
    public function beforeValidate(){
            return true;
    }
    /**
     * Creates data provider instance with search query applied
     *
     * @param array $params
     *
     * @return ActiveDataProvider
     */
    public function search($params)
    {
        $query = VendorProfileModel::find();

		        $dataProvider = new ActiveDataProvider([
            'query' => $query,
            'sort' => [
						'defaultOrder' => [
								'id' => SORT_DESC
						]
				]
        ]);

        if (!($this->load($params) && $this->validate())) {
            return $dataProvider;
        }

        $query->andFilterWhere([
            'id' => $this->id,
            'state_id' => $this->state_id,
            'type_id' => $this->type_id,
            'created_on' => $this->created_on,
            'updated_on' => $this->updated_on,
            'created_by_id' => $this->created_by_id,
        ]);

        $query->andFilterWhere(['like', 'title', $this->title])
            ->andFilterWhere(['like', 'first_name', $this->first_name])
            ->andFilterWhere(['like', 'last_name', $this->last_name])
            ->andFilterWhere(['like', 'civil_id', $this->civil_id])
            ->andFilterWhere(['like', 'description', $this->description])
            ->andFilterWhere(['like', 'whats_app_no', $this->whats_app_no])
            ->andFilterWhere(['like', 'shopname', $this->shopname])
            ->andFilterWhere(['like', 'shop_logo', $this->shop_logo]);

        return $dataProvider;
    }
}
